    "2. Use Lexa and direct project inspection to understand the affected files before editing.",
    "3. Apply focused fixes only for supported review findings.",
    "4. Run relevant verification commands when practical.",
    "5. Report what changed and what remains risky.",
    "6. Emit `FIX_VERDICT: fixed` only if every review finding is resolved; otherwise emit `FIX_VERDICT: not-fixed`.",
