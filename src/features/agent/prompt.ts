export function toReviewPrompt(
  mode: MenuItem,
  diffScope: DiffScopeItem,
  diff: GitDiffSnapshot,
): string {
  return [
    `Pipeline mode: ${mode.label}`,
    `Diff scope: ${diffScope.label}`,
    `Project path: ${diff.stats.cwd}`,
    `Changed files: ${diff.stats.changedFiles}`,
    `Added lines: ${diff.stats.addedLines}`,
    `Removed lines: ${diff.stats.removedLines}`,
    "",
    "Review workflow:",
    "1. Use the git diff below as the authoritative review scope.",
    "2. Use Lexa to understand changed files, dependencies, references, and architectural impact when available.",
    "3. Only report findings that are supported by the diff or project context.",
    "",
    "Git diff:",
    "```diff",
    diff.patch.length === 0 ? "(empty diff)" : diff.patch,
    "```",
    "",
    "Required Markdown structure:",
    "## Change Intention",
    "<Describe what this change appears to be trying to accomplish.>",
    "",
    "## Findings",
    "### Finding 1: <short title>",
    "- **Description:** <what is wrong>",
    "- **Level:** <major|minor>",
    "- **Risk:** <what can happen if this ships>",
    "- **Recommended fix:** <specific fix>",
    "",
    "## Verdicts",
    "- **Verdict:** <pass|needs changes>",
    "- **Reason:** <brief reason>",
  ].join("\n");
}

